
public class Ejercicio_II {

	public static void main(String[] args) {
		
		int contador = 2;
		
		while (contador <= 100) { 
			
			System.out.println("Valor de contador: " + contador);
			
			contador = contador + 2;
		
			
		}
		
		int contador2 = 100;
		
		while (contador2 >= 2) { 
			
			System.out.println("Valor de contador: " + contador2);
			
			contador2 = contador2 - 2;
			
		}

	}

}
